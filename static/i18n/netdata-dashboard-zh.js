  function translateByWords(value) {
    if (!value) return null;
    if (!/^[A-Za-z0-9 ,.!?()\-:%+/']+$/.test(value)) return null;
    WORD_REGEX.lastIndex = 0;
    let result = '';
    let lastIndex = 0;
    let changed = false;
    let match;
    while ((match = WORD_REGEX.exec(value)) !== null) {
      const prefix = value.slice(lastIndex, match.index);
      result += prefix;
      const word = match[0];
      const lower = word.toLowerCase();
      if (!lower) {
        result += word;
        lastIndex = match.index + word.length;
        continue;
      }
      if (lower.endsWith("'s")) {
        const base = lower.slice(0, -2);
        if (!base) {
          return null;
        }
        if (SKIP_WORDS.has(base)) {
          result += word.slice(0, -2) + " 的";
          changed = true;
        } else {
          const mapped = WORD_MAP.get(base);
          if (!mapped) {
            return null;
          }
          result += mapped + "的";
          changed = true;
        }
      } else if (lower.includes("'")) {
        return null;
      } else if (SKIP_WORDS.has(lower)) {
        result += word;
      } else {
        const mapped = WORD_MAP.get(lower);
        if (!mapped) {
          return null;
        }
        result += mapped;
        changed = true;
      }
      lastIndex = match.index + word.length;
    }
    result += value.slice(lastIndex);
    if (!changed) return null;
    result = result.replace(/([\u4e00-\u9fff])\s+([\u4e00-\u9fff])/g, '$1$2');
    result = result.replace(/([\u4e00-\u9fff])\s*,\s*/g, '$1，');
    result = result.replace(/,\s*([\u4e00-\u9fff])/g, '，$1');
    result = result.replace(/([\u4e00-\u9fff])\s*;\s*/g, '$1；');
    result = result.replace(/;\s*([\u4e00-\u9fff])/g, '；$1');
    result = result.replace(/([\u4e00-\u9fff])\s*:\s*/g, '$1：');
    result = result.replace(/:\s*([\u4e00-\u9fff])/g, '：$1');
    result = result.replace(/([\u4e00-\u9fff])\s*\.\s*/g, '$1。');
    result = result.replace(/\.\s*([\u4e00-\u9fff])/g, '。$1');
    result = result.replace(/([\u4e00-\u9fff])\s*\?\s*/g, '$1？');
    result = result.replace(/\?\s*([\u4e00-\u9fff])/g, '？$1');
    result = result.replace(/([\u4e00-\u9fff])\s*!\s*/g, '$1！');
    result = result.replace(/!\s*([\u4e00-\u9fff])/g, '！$1');
    result = result.replace(/\s+([，。！？；：])/g, '$1');
    result = result.replace(/([，。！？；：])\s+/g, '$1');
    return result.trim();
  }

  function getTranslation(raw) {
    if (!raw) return null;
    const normalized = normalize(raw);
    if (!normalized) return null;
    const exact = EXACT_TEXT.get(normalized);
    if (exact) return exact;
    for (const rule of REGEX_RULES) {
      const match = normalized.match(rule.pattern);
      if (match) {
        return rule.handler(match);
      }
    }
    const wordBased = translateByWords(normalized);
    if (wordBased) return wordBased;
    return null;
  }

  function translateTextNode(node) {
    if (!node || node.nodeType !== Node.TEXT_NODE) return;
    const value = node.textContent;
    if (!value) return;
    if (!/[A-Za-z]/.test(value)) return;
    if (hasHan(value)) return;
    const translation = getTranslation(value);
    if (!translation) return;
    const leading = (value.match(/^\s*/) || [""])[0];
    const trailing = (value.match(/\s*$/) || [""])[0];
    node.textContent = `${leading}${translation}${trailing}`;
  }

  function translateAttributes(element) {
    for (const name of ATTRIBUTE_NAMES) {
      if (!element.hasAttribute(name)) continue;
      const value = element.getAttribute(name);
      if (!value || hasHan(value)) continue;
      const translation = getTranslation(value);
      if (translation && translation !== value) {
        element.setAttribute(name, translation);
      }
    }
  }

  function translateSubtree(root) {
    if (!root) return;
    const walker = document.createTreeWalker(
      root,
      NodeFilter.SHOW_ELEMENT | NodeFilter.SHOW_TEXT,
      {
        acceptNode(node) {
          if (node.nodeType === Node.ELEMENT_NODE && SKIP_TAGS.has(node.tagName)) {
            return NodeFilter.FILTER_REJECT;
          }
          return NodeFilter.FILTER_ACCEPT;
        }
      }
    );

    let current = walker.currentNode;
    while (current) {
      if (current.nodeType === Node.TEXT_NODE) {
        translateTextNode(current);
      } else if (current.nodeType === Node.ELEMENT_NODE) {
        translateAttributes(current);
      }
      current = walker.nextNode();
    }
  }

  function handleMutations(mutations) {
    for (const mutation of mutations) {
      if (mutation.type === "childList") {
        mutation.addedNodes.forEach(node => {
          if (node.nodeType === Node.TEXT_NODE) {
            translateTextNode(node);
          } else if (node.nodeType === Node.ELEMENT_NODE) {
            translateSubtree(node);
          }
        });
      } else if (mutation.type === "characterData") {
        translateTextNode(mutation.target);
      } else if (mutation.type === "attributes") {
        translateAttributes(mutation.target);
      }
    }
  }

  function bootstrap() {
    if (bootstrap.started) return;
    bootstrap.started = true;
    if (document.documentElement && document.documentElement.lang !== LOCALE) {
      document.documentElement.lang = LOCALE;
    }
    translateSubtree(document.body);
    const observer = new MutationObserver(handleMutations);
    if (document.body) {
      observer.observe(document.body, {
        childList: true,
        subtree: true,
        characterData: true,
        attributes: true,
        attributeFilter: ATTRIBUTE_NAMES
      });
      window.__NETDATA_DASHBOARD_I18N_OBSERVER__ = observer;
    }
    window.__NETDATA_DASHBOARD_I18N__ = LOCALE;
    window.netdataDashboardLocale = LOCALE;
    window.netdataDashboardTranslate = getTranslation;
    console.info("[netdata-i18n] Applied zh-CN dashboard translations");
  }

